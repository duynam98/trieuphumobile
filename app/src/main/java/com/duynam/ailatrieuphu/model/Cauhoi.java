package com.duynam.ailatrieuphu.model;

public class Cauhoi {

    public String id;
    public String cauhoi;
    public String daA;
    public String daB;
    public String daC;
    public String daD;
    public String daDung;

    public Cauhoi() {
    }

    public Cauhoi(String id, String cauhoi, String daA, String daB, String daC, String daD, String daDung) {
        this.id = id;
        this.cauhoi = cauhoi;
        this.daA = daA;
        this.daB = daB;
        this.daC = daC;
        this.daD = daD;
        this.daDung = daDung;
    }
}
